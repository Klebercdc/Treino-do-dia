create extension if not exists pgcrypto;

create table if not exists public.diagnostic_admin_whitelist (
  email text primary key,
  created_at timestamptz not null default now()
);

create table if not exists public.diagnostic_executions (
  id bigint generated by default as identity primary key,
  execution_id uuid not null unique,
  user_id uuid null,
  session_id text null,
  is_admin_mode boolean not null default false,
  source text not null default 'chat',
  input_type text null,
  raw_input_summary text null,
  normalized_input_summary text null,
  intent_detected text null,
  intent_confidence numeric(6,5) null,
  pipeline_selected text null,
  decision_reason text null,
  fallback_used boolean not null default false,
  started_at timestamptz not null default now(),
  finished_at timestamptz null,
  duration_ms integer null,
  success boolean null,
  final_status text not null default 'running',
  error_code text null,
  error_message text null,
  response_summary text null,
  graph_path text null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.diagnostic_steps (
  id bigint generated by default as identity primary key,
  execution_id uuid not null,
  step_order integer not null,
  layer text not null default 'application',
  node_key text null,
  step_name text not null,
  status text not null default 'running',
  input_summary text null,
  output_summary text null,
  decision_reason text null,
  duration_ms integer null,
  success boolean null,
  error_code text null,
  error_message text null,
  prompt_signature text null,
  context_summary text null,
  dependency_summary text null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  constraint diagnostic_steps_execution_fk
    foreign key (execution_id)
    references public.diagnostic_executions(execution_id)
    on delete cascade,
  constraint diagnostic_steps_execution_order_unique unique (execution_id, step_order)
);

