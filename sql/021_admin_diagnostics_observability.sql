create or replace function public.set_diagnostic_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists trg_diagnostic_executions_updated_at on public.diagnostic_executions;
create trigger trg_diagnostic_executions_updated_at
before update on public.diagnostic_executions
for each row execute function public.set_diagnostic_updated_at();

create or replace function public.kronia_is_diagnostic_admin()
returns boolean
language sql
stable
as $$
  select
    coalesce(
      exists(
        select 1
        from public.profiles p
        where p.id = auth.uid()
          and lower(coalesce(p.role::text, '')) in ('admin', 'owner')
      ),
      false
    )
    or lower(coalesce(auth.jwt() -> 'app_metadata' ->> 'role', '')) in ('admin', 'owner')
    or exists(
      select 1
      from public.diagnostic_admin_whitelist w
      where lower(w.email) = lower(coalesce(auth.jwt() ->> 'email', auth.email()))
    );
$$;

