function _chip(group, value, label, active, multi) {
  var cls = multi ? 'dw-chip' : 'dw-chip-single';
  return '<button type="button" class="' + cls + (active ? ' active' : '') + '" data-group="' + group + '" data-value="' + value + '">' + label + '</button>';
}
